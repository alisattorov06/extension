function parseSavollar(matn) {
  return matn.trim().split("+++++").map(q => {
    const qator = q.trim().split("\n");
    const savol = qator[0];
    const variantlar = qator.slice(1);
    const togri = variantlar.find(v => v.startsWith("#"))?.replace("#", "").trim();
    return savol ? { savol, javob: togri } : null;
  }).filter(Boolean);
}

