chrome.runtime?.onInstalled?.addListener(() => {
  chrome.contextMenus.create({
    id: "javob-korsat",
    title: "Move",
    contexts: ["selection"]
  });
});

chrome.contextMenus?.onClicked?.addListener((info, tab) => {
  if (info.menuItemId === "javob-korsat") {
    chrome.scripting.executeScript({
      target: { tabId: tab.id },
      func: (text, db) => {
        function tozalash(str) {
          return str
            .toLowerCase()
            .replace(/[^\w\s]/g, "") // tinish belgilari OUT
            .replace(/\s+/g, " ")     // ortiqcha bo‘shliq OUT
            .trim();
        }

        const foySavol = tozalash(text);
        const foySozlar = new Set(foySavol.split(" "));

        let topilgan = null;
        for (const q of db) {
          const bazaSozlar = new Set(tozalash(q.savol).split(" "));
          let umumiy = 0;
          foySozlar.forEach(s => {
            if (bazaSozlar.has(s)) umumiy++;
          });
          if (umumiy >= 3) {
            topilgan = q;
            break;
          }
        }

        const javob = topilgan ? ` ${topilgan.javob}` : "Javob topilmadi";

        // Eskisini o‘chir
        document.getElementById("javob-blok")?.remove();

        // Blok yasash
        const div = document.createElement("div");
        div.id = "javob-blok";
        div.innerText = javob;
        Object.assign(div.style, {
          position: "fixed",
          bottom: "10px",
          left: "10px",
          color: "black",
          padding: "10px",
          fontSize: "14px",
          opacity: "0.7",
          zIndex: 999999,
          
        });


        document.body.appendChild(div);
        setTimeout(() => div.remove(), 2000); // 2 soniya ko‘rsatilsin
      },
      args: [info.selectionText, db]
    });
  }
});
